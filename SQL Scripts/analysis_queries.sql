-- summary stats comparing averages by category
SELECT 
    p.CATEGORY,
    ROUND(AVG(f.SHIPPING_COST), 2) AS MEAN_COST,
    ROUND(VARIANCE(f.SHIPPING_COST), 2) AS VAR_COST,
    COUNT(f.SHIPMENT_ID) AS SAMPLE_SIZE
FROM SUPPLY_CHAIN_DW.FACT_SHIPMENTS f
JOIN SUPPLY_CHAIN_DW.DIM_PRODUCTS p ON f.PRODUCT_ID = p.PRODUCT_ID
WHERE p.CATEGORY IN ('Electronics', 'Textiles', 'Automotive', 'Medical Supplies')
GROUP BY p.CATEGORY;

-- f-test comparing consistency
SELECT 
    s.SUPPLIER_NAME,
    VARIANCE(f.DELIVERY_DATE - f.SHIP_DATE) AS LEAD_TIME_VARIANCE
FROM SUPPLY_CHAIN_DW.FACT_SHIPMENTS f
JOIN SUPPLY_CHAIN_DW.DIM_SUPPLIER_DATA s ON f.PRODUCT_ID = s.PRODUCT_ID
GROUP BY s.SUPPLIER_NAME
fetch first 20 rows only;
